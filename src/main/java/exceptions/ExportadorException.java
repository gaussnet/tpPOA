package exceptions;

public class ExportadorException extends Exception {

	public ExportadorException() {
		super();
		
	}

	public ExportadorException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ExportadorException(String message) {
		super(message);
		
	}

	public ExportadorException(Throwable cause) {
		super(cause);
		
	}
}
